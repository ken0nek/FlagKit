extension FlagKit {

    public static func allFlagsAndNames() -> [(flag: NSImage?, name: String)] {
        return Array(zip(allFlags(), allNames()))
    }

    public static func allSpecialFlagsAndNames() -> [(flag: NSImage?, name: String)] {
        return Array(zip(allSpecialFlags(), allSpecialNames()))
    }
}

extension FlagKit {

    public static func flagAtCurrentLocale() -> NSImage? {
        return flagImage(flagCode: NSLocale.autoupdatingCurrentLocale().objectForKey(NSLocaleCountryCode) as! String)
    }
}

extension FlagKit {

    private static func flagImage(flagCode code: String) -> NSImage? {
        return NSImage(flagCode: code)
    }
}

public extension NSImage {

    public convenience init?(flagCode code: String) {
        guard let image = FlagKit.assetBundle.imageForResource(code) else { return nil }
        guard let CGImage = image.CGImageForProposedRect(nil, context: nil, hints: nil) else { return nil }
        self.init(CGImage:CGImage, size:image.size)
    }
}
