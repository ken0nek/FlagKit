extension FlagKit {
    
    public typealias Country = (flag: UIImage?, name: String)
    
    public static func allFlagsAndNames() -> [Country] {
        return Array(zip(FlagKit.allFlags(), FlagKit.allNames()))
    }
    
    public static func allSpecialFlagsAndNames() -> [Country] {
        return Array(zip(allSpecialFlags(), allSpecialNames()))
    }
}

extension FlagKit {
    
    public static func flagAtCurrentLocale() -> UIImage? {
        return flagImage(flagCode: NSLocale.autoupdatingCurrentLocale().objectForKey(NSLocaleCountryCode) as! String)
    }
}

extension FlagKit {
    
    private static func flagImage(flagCode code: String) -> UIImage? {
        return UIImage(flagCode: code)
    }
}

public extension UIImage {
    
    public convenience init?(flagCode code: String) {
        self.init(named: code, inBundle: FlagKit.assetBundle, compatibleWithTraitCollection: nil)
    }
}